export const applyStyle = (text, styleNum) => {
    // Cas spécial pour le reverse
    if (styleNum === 50) return text.split('').reverse().join('');
    
    const map = maps[styleNum];
    if (!map) return text;
    
    return text.split('').map(char => {
        const index = alpha.indexOf(char);
        return index !== -1 ? map[index] : char;
    }).join('');
};

// Exporte aussi la liste pour ton menu d'aide
export const getStyleList = () => {
    let msg = '╭─── *STYLES DISPONIBLES* ───\n';
    Object.keys(maps).forEach((num) => {
        msg += `│ *${num}.* ${applyStyle("Exemple", parseInt(num))}\n`;
    });
    msg += `│ *50.* ${applyStyle("Exemple", 50)}\n`;
    msg += `╰─────────────────────`;
    return msg;
};
